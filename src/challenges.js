// Desafio 6
function highestCount(array) {
  let maior = array[0];
  let repeat = 1;
  for (let i = 1; i < array.length; i += 1) {
    if (maior === array[i]) {
      repeat += 1;
    }
    if (maior < array[i]) {
      maior = array[i];
      repeat = 1;
    }
  }
  return repeat;
}

